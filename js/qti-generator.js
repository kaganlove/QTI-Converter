/**
 * QTI Converter - QTI Generator Module
 * Generates Canvas-compliant QTI 1.2 XML files and bundles them into a ZIP.
 */

// Simple HTML/XML escaper
function escapeXml(unsafe) {
  if (unsafe === undefined || unsafe === null) return '';
  return unsafe.toString()
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

// Simple Markdown to HTML converter to preserve formatting in Canvas
function mdToHtml(mdText) {
  if (!mdText) return '';
  
  let html = escapeXml(mdText);

  // Bold (**text** or __text__)
  html = html.replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>');
  html = html.replace(/__(.*?)__/g, '<strong>$1</strong>');

  // Italic (*text* or _text_)
  html = html.replace(/\*(.*?)\*/g, '<em>$1</em>');
  html = html.replace(/_(.*?)_/g, '<em>$1</em>');

  // Code (`code`)
  html = html.replace(/`(.*?)`/g, '<code>$1</code>');

  // Lists
  // Unordered
  html = html.replace(/^\s*-\s+(.*)$/gm, '<li>$1</li>');
  html = html.replace(/(<li>.*<\/li>)/s, '<ul>$1</ul>');
  
  // Convert newlines to breaks
  html = html.replace(/\n/g, '<br />');

  return html;
}

export class QTIGenerator {
  /**
   * Generates a zip package containing QTI files for Canvas.
   * @param {Array} questions - List of parsed question objects
   * @param {Object} options - Quiz configuration options
   * @returns {Promise<Blob>} ZIP file Blob
   */
  async generateZip(questions, options = {}) {
    const quizTitle = options.title || 'QTI Question Bank';
    const quizDescription = options.description || 'Generated by QTI Converter';
    
    // Canvas metadata settings
    const shuffleAnswers = options.shuffleAnswers ? 'true' : 'false';
    const showCorrectAnswers = options.showCorrectAnswers ? 'true' : 'false';
    const oneQuestionAtATime = options.oneQuestionAtATime ? 'true' : 'false';
    const cantGoBack = options.cantGoBack ? 'true' : 'false';

    const timestamp = Date.now();
    const idBase = 'qti_conv';
    const manifestId = `${idBase}_manifest_${timestamp}`;
    const assessmentId = `${idBase}_assessment_${timestamp}`;
    const dependencyId = `${idBase}_dependency_${timestamp}`;
    const assignmentId = `${idBase}_assignment_${timestamp}`;
    const assignmentGroupId = `${idBase}_assignment-group_${timestamp}`;

    // Calculate total points
    const totalPoints = questions.reduce((acc, q) => acc + (q.points || 1), 0);

    // 1. Generate imsmanifest.xml
    const dateStr = new Date().toISOString().split('T')[0];
    const manifestXml = `<?xml version="1.0" encoding="UTF-8"?>
<manifest identifier="${manifestId}" xmlns="http://www.imsglobal.org/xsd/imsccv1p1/imscp_v1p1" xmlns:lom="http://ltsc.ieee.org/xsd/imsccv1p1/LOM/resource" xmlns:imsmd="http://www.imsglobal.org/xsd/imsmd_v1p2" xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance" xsi:schemaLocation="http://www.imsglobal.org/xsd/imsccv1p1/imscp_v1p1 http://www.imsglobal.org/xsd/imscp_v1p1.xsd http://ltsc.ieee.org/xsd/imsccv1p1/LOM/resource http://www.imsglobal.org/profile/cc/ccv1p1/LOM/ccv1p1_lomresource_v1p0.xsd http://www.imsglobal.org/xsd/imsmd_v1p2 http://www.imsglobal.org/xsd/imsmd_v1p2p2.xsd">
  <metadata>
    <schema>IMS Content</schema>
    <schemaversion>1.1.3</schemaversion>
    <imsmd:lom>
      <imsmd:general>
        <imsmd:title>
          <imsmd:string>QTI assessment generated by QTI Converter</imsmd:string>
        </imsmd:title>
      </imsmd:general>
      <imsmd:lifeCycle>
        <imsmd:contribute>
          <imsmd:date>
            <imsmd:dateTime>${dateStr}</imsmd:dateTime>
          </imsmd:date>
        </imsmd:contribute>
      </imsmd:lifeCycle>
      <imsmd:rights>
        <imsmd:copyrightAndOtherRestrictions>
          <imsmd:value>yes</imsmd:value>
        </imsmd:copyrightAndOtherRestrictions>
        <imsmd:description>
          <imsmd:string>Private (Copyrighted)</imsmd:string>
        </imsmd:description>
      </imsmd:rights>
    </imsmd:lom>
  </metadata>
  <organizations/>
  <resources>
    <resource identifier="${assessmentId}" type="imsqti_xmlv1p2">
      <file href="${assessmentId}/${assessmentId}.xml"/>
      <dependency identifierref="${dependencyId}"/>
    </resource>
    <resource identifier="${dependencyId}" type="associatedcontent/imscc_xmlv1p1/learning-application-resource" href="${assessmentId}/assessment_meta.xml">
      <file href="${assessmentId}/assessment_meta.xml"/>
    </resource>
  </resources>
</manifest>`;

    // 2. Generate assessment_meta.xml
    const hideResults = showCorrectAnswers === 'false' ? 'always' : '';
    const metaXml = `<?xml version="1.0" encoding="UTF-8"?>
<quiz identifier="${assessmentId}" xmlns="http://canvas.instructure.com/xsd/cccv1p0" xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance" xsi:schemaLocation="http://canvas.instructure.com/xsd/cccv1p0 https://canvas.instructure.com/xsd/cccv1p0.xsd">
  <title>${escapeXml(quizTitle)}</title>
  <description>${escapeXml(mdToHtml(quizDescription))}</description>
  <shuffle_answers>${shuffleAnswers}</shuffle_answers>
  <scoring_policy>keep_highest</scoring_policy>
  <hide_results>${hideResults}</hide_results>
  <quiz_type>assignment</quiz_type>
  <points_possible>${totalPoints.toFixed(1)}</points_possible>
  <require_lockdown_browser>false</require_lockdown_browser>
  <require_lockdown_browser_for_results>false</require_lockdown_browser_for_results>
  <require_lockdown_browser_monitor>false</require_lockdown_browser_monitor>
  <lockdown_browser_monitor_data/>
  <show_correct_answers>${showCorrectAnswers}</show_correct_answers>
  <anonymous_submissions>false</anonymous_submissions>
  <could_be_locked>false</could_be_locked>
  <allowed_attempts>1</allowed_attempts>
  <one_question_at_a_time>${oneQuestionAtATime}</one_question_at_a_time>
  <cant_go_back>${cantGoBack}</cant_go_back>
  <available>false</available>
  <one_time_results>false</one_time_results>
  <show_correct_answers_last_attempt>false</show_correct_answers_last_attempt>
  <only_visible_to_overrides>false</only_visible_to_overrides>
  <module_locked>false</module_locked>
  <assignment identifier="${assignmentId}">
    <title>${escapeXml(quizTitle)}</title>
    <due_at/>
    <lock_at/>
    <unlock_at/>
    <module_locked>false</module_locked>
    <workflow_state>unpublished</workflow_state>
    <assignment_overrides>
    </assignment_overrides>
    <quiz_identifierref>${assessmentId}</quiz_identifierref>
    <allowed_extensions></allowed_extensions>
    <has_group_category>false</has_group_category>
    <points_possible>${totalPoints.toFixed(1)}</points_possible>
    <grading_type>points</grading_type>
    <all_day>false</all_day>
    <submission_types>online_quiz</submission_types>
    <position>1</position>
    <turnitin_enabled>false</turnitin_enabled>
    <vericite_enabled>false</vericite_enabled>
    <peer_review_count>0</peer_review_count>
    <peer_reviews>false</peer_reviews>
    <automatic_peer_reviews>false</automatic_peer_reviews>
    <anonymous_peer_reviews>false</anonymous_peer_reviews>
    <grade_group_students_individually>false</grade_group_students_individually>
    <freeze_on_copy>false</freeze_on_copy>
    <omit_from_final_grade>false</omit_from_final_grade>
    <intra_group_peer_reviews>false</intra_group_peer_reviews>
    <only_visible_to_overrides>false</only_visible_to_overrides>
    <post_to_sis>false</post_to_sis>
    <moderated_grading>false</moderated_grading>
    <grader_count>0</grader_count>
    <grader_comments_visible_to_graders>true</grader_comments_visible_to_graders>
    <anonymous_grading>false</anonymous_grading>
    <graders_anonymous_to_graders>false</graders_anonymous_to_graders>
    <grader_names_visible_to_final_grader>true</grader_names_visible_to_final_grader>
    <anonymous_instructor_annotations>false</anonymous_instructor_annotations>
    <post_policy>
      <post_manually>false</post_manually>
    </post_policy>
  </assignment>
  <assignment_group_identifierref>${assignmentGroupId}</assignment_group_identifierref>
  <assignment_overrides>
  </assignment_overrides>
</quiz>`;

    // 3. Generate assessment.xml
    let assessmentXml = `<?xml version="1.0" encoding="UTF-8"?>
<questestinterop xmlns="http://www.imsglobal.org/xsd/ims_qtiasiv1p2" xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance" xsi:schemaLocation="http://www.imsglobal.org/xsd/ims_qtiasiv1p2 http://www.imsglobal.org/xsd/ims_qtiasiv1p2p1.xsd">
  <assessment ident="${assessmentId}" title="${escapeXml(quizTitle)}">
    <qtimetadata>
      <qtimetadatafield>
        <fieldlabel>cc_maxattempts</fieldlabel>
        <fieldentry>1</fieldentry>
      </qtimetadatafield>
    </qtimetadata>
    <section ident="root_section">`;

    // Process questions
    questions.forEach((q, idx) => {
      const qId = `question_${idx + 1}_${timestamp}`;
      const qRefId = `question_ref_${idx + 1}_${timestamp}`;
      const qTextHtml = mdToHtml(q.text);
      const qTitleXml = escapeXml(q.title || `Question ${idx + 1}`);

      assessmentXml += `
      <item ident="${qId}" title="${qTitleXml}">
        <itemmetadata>
          <qtimetadata>
            <qtimetadatafield>
              <fieldlabel>question_type</fieldlabel>
              <fieldentry>${q.type}</fieldentry>
            </qtimetadatafield>
            <qtimetadatafield>
              <fieldlabel>points_possible</fieldlabel>
              <fieldentry>${q.points}</fieldentry>
            </qtimetadatafield>`;

      // Add choice answers array in metadata if multiple choices exist
      if (['multiple_choice_question', 'true_false_question', 'multiple_answers_question'].includes(q.type)) {
        const choiceIds = q.choices.map((_, cIdx) => `choice_${cIdx + 1}_${qId}`).join(',');
        assessmentXml += `
            <qtimetadatafield>
              <fieldlabel>original_answer_ids</fieldlabel>
              <fieldentry>${choiceIds}</fieldentry>
            </qtimetadatafield>`;
      } else if (q.type === 'matching_question') {
        const stemIds = q.matches.map((_, sIdx) => `stem_${sIdx + 1}_${qId}`).join(',');
        assessmentXml += `
            <qtimetadatafield>
              <fieldlabel>original_answer_ids</fieldlabel>
              <fieldentry>${stemIds}</fieldentry>
            </qtimetadatafield>`;
      } else if (q.type === 'numerical_question') {
        assessmentXml += `
            <qtimetadatafield>
              <fieldlabel>original_answer_ids</fieldlabel>
              <fieldentry>numerical_${qId}</fieldentry>
            </qtimetadatafield>`;
      } else if (q.type === 'essay_question') {
        assessmentXml += `
            <qtimetadatafield>
              <fieldlabel>original_answer_ids</fieldlabel>
              <fieldentry></fieldentry>
            </qtimetadatafield>`;
      } else if (q.type === 'file_upload_question') {
        assessmentXml += `
            <qtimetadatafield>
              <fieldlabel>original_answer_ids</fieldlabel>
              <fieldentry></fieldentry>
            </qtimetadatafield>`;
      }

      assessmentXml += `
            <qtimetadatafield>
              <fieldlabel>assessment_question_identifierref</fieldlabel>
              <fieldentry>${qRefId}</fieldentry>
            </qtimetadatafield>
          </qtimetadata>
        </itemmetadata>
        <presentation>
          <material>
            <mattext texttype="text/html">${escapeXml(qTextHtml)}</mattext>
          </material>`;

      // PRESENTATION based on question type
      if (['multiple_choice_question', 'true_false_question', 'multiple_answers_question'].includes(q.type)) {
        const cardinality = q.type === 'multiple_answers_question' ? 'Multiple' : 'Single';
        assessmentXml += `
          <response_lid ident="response1" rcardinality="${cardinality}">
            <render_choice>`;

        q.choices.forEach((choice, cIdx) => {
          const choiceId = `choice_${cIdx + 1}_${qId}`;
          assessmentXml += `
              <response_label ident="${choiceId}">
                <material>
                  <mattext texttype="text/html">${escapeXml(mdToHtml(choice.text))}</mattext>
                </material>
              </response_label>`;
        });

        assessmentXml += `
            </render_choice>
          </response_lid>`;
      } else if (q.type === 'short_answer_question' || q.type === 'essay_question') {
        assessmentXml += `
          <response_str ident="response1" rcardinality="Single">
            <render_fib>
              <response_label ident="answer1" rshuffle="No"/>
            </render_fib>
          </response_str>`;
      } else if (q.type === 'numerical_question') {
        assessmentXml += `
          <response_str ident="response1" rcardinality="Single">
            <render_fib fibtype="Decimal">
              <response_label ident="answer1"/>
            </render_fib>
          </response_str>`;
      } else if (q.type === 'matching_question') {
        const allRight = [...new Set([...q.matches.map(m => m.right), ...q.distractors])];
        const matchOptions = allRight.map((text, cIdx) => ({
          id: `match_${cIdx + 1}_${qId}`,
          text: text
        }));
        
        q.matchOptions = matchOptions;
        
        q.matches.forEach((matchPair, sIdx) => {
          const stemId = `stem_${sIdx + 1}_${qId}`;
          assessmentXml += `
          <response_lid ident="response_${stemId}" rcardinality="Single">
            <material>
              <mattext texttype="text/html">${escapeXml(mdToHtml(matchPair.left))}</mattext>
            </material>
            <render_choice>`;
            
          matchOptions.forEach(option => {
            assessmentXml += `
              <response_label ident="${option.id}">
                <material>
                  <mattext texttype="text/html">${escapeXml(mdToHtml(option.text))}</mattext>
                </material>
              </response_label>`;
          });
          
          assessmentXml += `
            </render_choice>
          </response_lid>`;
        });
      }

      assessmentXml += `
        </presentation>
        <resprocessing>
          <outcomes>
            <decvar maxvalue="100" minvalue="0" varname="SCORE" vartype="Decimal"/>
          </outcomes>`;

      // General Question Feedback
      if (q.feedback) {
        assessmentXml += `
          <respcondition continue="Yes">
            <conditionvar>
              <other/>
            </conditionvar>
            <displayfeedback feedbacktype="Response" linkrefid="general_fb"/>
          </respcondition>`;
      }

      // Choice-specific Feedback mapping
      if (['multiple_choice_question', 'true_false_question', 'multiple_answers_question'].includes(q.type)) {
        q.choices.forEach((choice, cIdx) => {
          if (choice.feedback) {
            const choiceId = `choice_${cIdx + 1}_${qId}`;
            assessmentXml += `
          <respcondition continue="Yes">
            <conditionvar>
              <varequal respident="response1">${choiceId}</varequal>
            </conditionvar>
            <displayfeedback feedbacktype="Response" linkrefid="${choiceId}_fb"/>
          </respcondition>`;
          }
        });
      }

      // RESPOND CONDITION - SCORING AND CORRECT ANSWERS
      if (['multiple_choice_question', 'true_false_question'].includes(q.type)) {
        const correctChoiceIdx = q.choices.findIndex(c => c.correct);
        if (correctChoiceIdx !== -1) {
          const correctChoiceId = `choice_${correctChoiceIdx + 1}_${qId}`;
          const feedbackRef = q.correctFeedback ? 'correct_fb' : '';

          assessmentXml += `
          <respcondition continue="No">
            <conditionvar>
              <varequal respident="response1">${correctChoiceId}</varequal>
            </conditionvar>
            <setvar action="Set" varname="SCORE">100</setvar>${feedbackRef ? `\n            <displayfeedback feedbacktype="Response" linkrefid="${feedbackRef}"/>` : ''}
          </respcondition>`;
        }
      } else if (q.type === 'matching_question') {
        const feedbackRef = q.correctFeedback ? 'correct_fb' : '';
        const pointsPerMatch = (100 / q.matches.length).toFixed(2);
        
        q.matches.forEach((matchPair, sIdx) => {
          const stemId = `stem_${sIdx + 1}_${qId}`;
          const correctOption = q.matchOptions.find(o => o.text === matchPair.right);
          if (correctOption) {
            assessmentXml += `
          <respcondition continue="Yes">
            <conditionvar>
              <varequal respident="response_${stemId}">${correctOption.id}</varequal>
            </conditionvar>
            <setvar action="Add" varname="SCORE">${pointsPerMatch}</setvar>
          </respcondition>`;
          }
        });
        
        if (feedbackRef) {
          assessmentXml += `
          <respcondition continue="Yes">
            <conditionvar>
              <and>`;
          q.matches.forEach((matchPair, sIdx) => {
            const stemId = `stem_${sIdx + 1}_${qId}`;
            const correctOption = q.matchOptions.find(o => o.text === matchPair.right);
            if (correctOption) {
              assessmentXml += `
                <varequal respident="response_${stemId}">${correctOption.id}</varequal>`;
            }
          });
          assessmentXml += `
              </and>
            </conditionvar>
            <displayfeedback feedbacktype="Response" linkrefid="${feedbackRef}"/>
          </respcondition>`;
        }
      } else if (q.type === 'multiple_answers_question') {
        const feedbackRef = q.correctFeedback ? 'correct_fb' : '';
        assessmentXml += `
          <respcondition continue="No">
            <conditionvar>
              <and>`;

        q.choices.forEach((choice, cIdx) => {
          const choiceId = `choice_${cIdx + 1}_${qId}`;
          if (choice.correct) {
            assessmentXml += `
                <varequal respident="response1">${choiceId}</varequal>`;
          } else {
            assessmentXml += `
                <not>
                  <varequal respident="response1">${choiceId}</varequal>
                </not>`;
          }
        });

        assessmentXml += `
              </and>
            </conditionvar>
            <setvar action="Set" varname="SCORE">100</setvar>${feedbackRef ? `\n            <displayfeedback feedbacktype="Response" linkrefid="${feedbackRef}"/>` : ''}
          </respcondition>`;
      } else if (q.type === 'short_answer_question') {
        const feedbackRef = q.correctFeedback ? 'correct_fb' : '';
        assessmentXml += `
          <respcondition continue="No">
            <conditionvar>`;

        q.choices.forEach(choice => {
          assessmentXml += `
              <varequal respident="response1">${escapeXml(choice.text)}</varequal>`;
        });

        assessmentXml += `
            </conditionvar>
            <setvar action="Set" varname="SCORE">100</setvar>${feedbackRef ? `\n            <displayfeedback feedbacktype="Response" linkrefid="${feedbackRef}"/>` : ''}
          </respcondition>`;
      } else if (q.type === 'numerical_question') {
        const feedbackRef = q.correctFeedback ? 'correct_fb' : '';
        const exact = q.numericalExact !== undefined && q.numericalExact !== null;

        assessmentXml += `
          <respcondition continue="No">
            <conditionvar>`;

        if (exact) {
          assessmentXml += `
              <or>
                <varequal respident="response1">${q.numericalExact}</varequal>
                <and>
                  <vargte respident="response1">${q.numericalMin}</vargte>
                  <varlte respident="response1">${q.numericalMax}</varlte>
                </and>
              </or>`;
        } else {
          assessmentXml += `
              <and>
                <vargte respident="response1">${q.numericalMin}</vargte>
                <varlte respident="response1">${q.numericalMax}</varlte>
              </and>`;
        }

        assessmentXml += `
            </conditionvar>
            <setvar action="Set" varname="SCORE">100</setvar>${feedbackRef ? `\n            <displayfeedback feedbacktype="Response" linkrefid="${feedbackRef}"/>` : ''}
          </respcondition>`;
      } else if (q.type === 'essay_question') {
        // Essay questions are graded manually in Canvas, scoring logic is simpler
        assessmentXml += `
          <respcondition continue="No">
            <conditionvar>
              <other/>
            </conditionvar>
          </respcondition>`;
      }

      // Incorrect Feedback response mapping
      if (q.incorrectFeedback) {
        assessmentXml += `
          <respcondition continue="Yes">
            <conditionvar>
              <other/>
            </conditionvar>
            <displayfeedback feedbacktype="Response" linkrefid="general_incorrect_fb"/>
          </respcondition>`;
      }

      assessmentXml += `
        </resprocessing>`;

      // FEEDBACK CONTENT BLOCK
      if (q.feedback) {
        assessmentXml += `
        <itemfeedback ident="general_fb">
          <flow_mat>
            <material>
              <mattext texttype="text/html">${escapeXml(mdToHtml(q.feedback))}</mattext>
            </material>
          </flow_mat>
        </itemfeedback>`;
      }
      if (q.correctFeedback) {
        assessmentXml += `
        <itemfeedback ident="correct_fb">
          <flow_mat>
            <material>
              <mattext texttype="text/html">${escapeXml(mdToHtml(q.correctFeedback))}</mattext>
            </material>
          </flow_mat>
        </itemfeedback>`;
      }
      if (q.incorrectFeedback) {
        assessmentXml += `
        <itemfeedback ident="general_incorrect_fb">
          <flow_mat>
            <material>
              <mattext texttype="text/html">${escapeXml(mdToHtml(q.incorrectFeedback))}</mattext>
            </material>
          </flow_mat>
        </itemfeedback>`;
      }

      // Choice specific feedback definition blocks
      if (['multiple_choice_question', 'true_false_question', 'multiple_answers_question'].includes(q.type)) {
        q.choices.forEach((choice, cIdx) => {
          if (choice.feedback) {
            const choiceId = `choice_${cIdx + 1}_${qId}`;
            assessmentXml += `
        <itemfeedback ident="${choiceId}_fb">
          <flow_mat>
            <material>
              <mattext texttype="text/html">${escapeXml(mdToHtml(choice.feedback))}</mattext>
            </material>
          </flow_mat>
        </itemfeedback>`;
          }
        });
      }

      assessmentXml += `
      </item>`;
    });

    assessmentXml += `
    </section>
  </assessment>
</questestinterop>`;

    // 4. Build ZIP file using JSZip
    // JSZip is expected to be loaded in index.html, we will pass it or fetch from global window.JSZip
    const JSZip = window.JSZip;
    if (!JSZip) {
      throw new Error("JSZip library is not loaded. Please ensure you are online or JSZip is included.");
    }

    const zip = new JSZip();
    zip.file("imsmanifest.xml", manifestXml);
    
    const assessmentFolder = zip.folder(assessmentId);
    assessmentFolder.file(`${assessmentId}.xml`, assessmentXml);
    assessmentFolder.file("assessment_meta.xml", metaXml);

    const zipBlob = await zip.generateAsync({ type: "blob" });
    return {
      blob: zipBlob,
      filename: `${quizTitle.replace(/[^a-z0-9]/gi, '_').toLowerCase()}_qti.zip`
    };
  }
}
